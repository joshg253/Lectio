def _scrape_change_detect(conn: sqlite3.Connection, feed: dict) -> bool:
    """Hash-compare page (or selected element) content. Creates an entry when content changes."""
    try:
        html = _fetch_html(str(feed["source_url"]))
    except Exception as exc:
        LOGGER.warning("[scraper] fetch failed for %s: %s", feed["source_url"], exc)
        return False

    selector = str(feed.get("selector") or "").strip()
    if selector:
        try:
            soup = BeautifulSoup(html, "html.parser")
            content = "".join(str(el) for el in soup.select(selector))
        except Exception:
            content = html
    else:
        content = html

    new_hash = hashlib.sha256(content.encode()).hexdigest()
    now = datetime.now(timezone.utc).isoformat()

    if new_hash == feed.get("last_content_hash"):
        conn.execute("UPDATE scraped_feeds SET last_scraped_at = ? WHERE id = ?", (now, feed["id"]))
        return False

    page_title = _page_title(html, str(feed["feed_title"]))
    entry_id = str(uuid.uuid4())
    conn.execute(
        "INSERT INTO scraped_entries (id, scraped_feed_id, entry_url, title, content, published_at, hidden)"
        " VALUES (?, ?, ?, ?, ?, ?, 0)",
        (entry_id, feed["id"], feed["source_url"], f"{page_title} — updated", content[:_MAX_CONTENT_BYTES], now),
    )
    conn.execute(
        "UPDATE scraped_feeds SET last_content_hash = ?, last_scraped_at = ? WHERE id = ?",
        (new_hash, now, feed["id"]),
    )
    return True


def _scrape_link_list(conn: sqlite3.Connection, feed: dict, initial: bool = False) -> bool:
    """Extract links; add new ones as (optionally hidden) entries. Returns True if new visible entries added."""
    try:
        html = _fetch_html(str(feed["source_url"]))
    except Exception as exc:
        LOGGER.warning("[scraper] fetch failed for %s: %s", feed["source_url"], exc)
        return False

    selector = str(feed.get("selector") or "").strip()
    soup = BeautifulSoup(html, "html.parser")
    link_elements = _resolve_link_anchors(soup, selector)

    existing_urls: set[str] = {
        str(r["entry_url"])
        for r in conn.execute(
            "SELECT entry_url FROM scraped_entries WHERE scraped_feed_id = ?", (feed["id"],)
        ).fetchall()
    }

    now = datetime.now(timezone.utc).isoformat()
    new_visible = 0

    for a in link_elements:
        item = _anchor_to_item(a, str(feed["source_url"]))
        if not item:
            continue
        abs_url = item["url"]
        if abs_url in existing_urls:
            continue
        existing_urls.add(abs_url)
        title = item["title"]
        entry_id = str(uuid.uuid4())
        hidden = 1 if initial else 0
        conn.execute(
            "INSERT OR IGNORE INTO scraped_entries"
            " (id, scraped_feed_id, entry_url, title, content, published_at, hidden)"
            " VALUES (?, ?, ?, ?, ?, ?, ?)",
            (entry_id, feed["id"], abs_url, title, "", now, hidden),
        )
        if not initial:
            new_visible += 1

    conn.execute("UPDATE scraped_feeds SET last_scraped_at = ? WHERE id = ?", (now, feed["id"]))
    return new_visible > 0


