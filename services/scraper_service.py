def preview_page_feed(source_url: str, mode: str, selector: str | None) -> dict:
    """Preview what a page feed would produce, without creating anything.

    Returns ``{title, mode, items, suggestions, content_preview}``:
    - ``items``: link-list entries [{title, url}] the selector would yield;
    - ``suggestions``: ranked candidate selectors for link_list mode;
    - ``content_preview``: text excerpt of the watched region for change_detect.
    Raises on fetch failure so the route can surface the error."""
    html = _fetch_html(source_url)
    title = _page_title(html, source_url)
    sel = (selector or "").strip()
    out: dict = {"title": title, "mode": mode, "items": [], "suggestions": [], "content_preview": ""}
    if mode == "link_list":
        out["suggestions"] = suggest_selectors(html, source_url)
        if sel:
            out["items"] = extract_link_items(html, source_url, sel)[:50]
    else:
        try:
            soup = BeautifulSoup(html, "html.parser")
            content = "".join(str(el) for el in soup.select(sel)) if sel else html
        except Exception:
            content = html
        text = BeautifulSoup(content, "html.parser").get_text(" ", strip=True)
        out["content_preview"] = text[:600]
    return out


def create_scraped_feed(
    conn: sqlite3.Connection,
    reader,
    source_url: str,
    mode: str,
    selector: str | None,
    feed_title: str | None,
    backfill: bool = False,
) -> tuple[str, str]:
    """Create a scraped feed, do initial scrape, register with reader. Returns (feed_id, file_url).

    When ``backfill`` is set (link_list only), pre-existing links are surfaced as
    visible entries rather than seeded hidden.

    The caller is responsible for adding the feed to a folder in folder_feeds.
    """
    assert mode in ("change_detect", "link_list"), f"unknown scraping mode: {mode!r}"

    feed_id = str(uuid.uuid4())
    now = datetime.now(timezone.utc).isoformat()

    if not feed_title:
        try:
            html = _fetch_html(source_url)
            feed_title = _page_title(html, source_url)
        except Exception:
            feed_title = source_url

    conn.execute(
        "INSERT INTO scraped_feeds (id, source_url, mode, selector, feed_title, created_at)"
        " VALUES (?, ?, ?, ?, ?, ?)",
        (feed_id, source_url, mode, selector or None, feed_title, now),
    )

    # Write empty XML first so reader can open the file immediately.
    _write_empty_feed_file(feed_id, feed_title, source_url)

    feed_row = conn.execute("SELECT * FROM scraped_feeds WHERE id = ?", (feed_id,)).fetchone()
    feed = dict(feed_row)

    if mode == "link_list":
        # Seed existing links as hidden so they aren't surfaced as new entries,
        # unless backfill was requested — then surface them as visible entries.
        _scrape_link_list(conn, feed, initial=not backfill)
    else:
        # Capture initial hash and create a first entry so the feed isn't empty on
        # first view — subsequent page changes will generate additional entries.
        try:
            html = _fetch_html(source_url)
            sel = (selector or "").strip()
            if sel:
                try:
                    soup = BeautifulSoup(html, "html.parser")
                    content = "".join(str(el) for el in soup.select(sel))
                except Exception:
                    content = html
            else:
                content = html
            initial_hash = hashlib.sha256(content.encode()).hexdigest()
            conn.execute(
                "UPDATE scraped_feeds SET last_content_hash = ?, last_scraped_at = ? WHERE id = ?",
                (initial_hash, now, feed_id),
            )
            conn.execute(
                "INSERT INTO scraped_entries (id, scraped_feed_id, title, entry_url, content, published_at)"
                " VALUES (?, ?, ?, ?, ?, ?)",
                (str(uuid.uuid4()), feed_id, feed_title, source_url, content[:_MAX_CONTENT_BYTES], now),
            )
        except Exception as exc:
            LOGGER.warning("[scraper] initial setup failed for %s: %s", source_url, exc)

    _write_feed_file(conn, feed_id)

    file_url = feed_file_url(feed_id)
    reader.add_feed(file_url, exist_ok=True)
    try:
        reader.update_feed(file_url)
    except Exception:
        pass
    return feed_id, file_url


def delete_scraped_feed(conn: sqlite3.Connection, reader, feed_id: str) -> None:
    """Remove a scraped feed: clean up DB rows, delete XML file, unsubscribe from reader."""
    assert_safe_feed_id(feed_id)
    file_url = feed_file_url(feed_id)
    conn.execute("DELETE FROM scraped_entries WHERE scraped_feed_id = ?", (feed_id,))
    conn.execute("DELETE FROM scraped_feeds WHERE id = ?", (feed_id,))
    try:
        (_dir() / f"{feed_id}.xml").unlink(missing_ok=True)
    except Exception:
        pass
    try:
        reader.delete_feed(file_url, missing_ok=True)
    except Exception:
        pass


def refresh_scraped_feed_by_id(conn: sqlite3.Connection, feed_id: str) -> bool:
    """Refresh a single scraped feed and rewrite its XML file. Returns True if new visible entries."""
    row = conn.execute("SELECT * FROM scraped_feeds WHERE id = ?", (feed_id,)).fetchone()
    if not row:
        return False
    feed = dict(row)
    if feed["mode"] == "change_detect":
        changed = _scrape_change_detect(conn, feed)
    else:
        changed = _scrape_link_list(conn, feed)
    _write_feed_file(conn, feed_id)
    return changed


def refresh_all_scraped_feeds(conn: sqlite3.Connection) -> None:
    """Refresh every scraped feed and rewrite its XML file."""
    try:
        rows = conn.execute("SELECT id FROM scraped_feeds").fetchall()
    except Exception:
        return  # table may not exist in test environments
    for row in rows:
        try:
            refresh_scraped_feed_by_id(conn, str(row["id"]))
        except Exception:
            LOGGER.exception("[scraper] error refreshing feed %s", row["id"])


def get_all_scraped_feeds(conn: sqlite3.Connection) -> list[dict]:
    """Return metadata for all scraped feeds."""
    return [dict(r) for r in conn.execute("SELECT * FROM scraped_feeds ORDER BY feed_title").fetchall()]
